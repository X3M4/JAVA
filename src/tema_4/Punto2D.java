package tema_4;

public class Punto2D {
    
    public double x, y;

   
   
    public Punto2D() {
        this.x = 0;
        this.y = 0;
    }
    public Punto2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }
}



